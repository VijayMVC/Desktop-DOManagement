    private void loadRoomInfo(Room roomDetails){
        jLb_RoomNo.setText(roomDetails.getRoomNumber());
        jCb_Status.setSelectedIndex(roomDetails.getStatus());
        int isPay = 0;
        if(roomDetails.isPaystatus() == true){
            isPay = 1;
        }
        jCb_PaymentStatus.setSelectedIndex(isPay);
        jLb_TotalSlot.setText(roomDetails.getNumberStudent() + roomDetails.getFreeSlot() + "");
        jLb_NumberOfStudents.setText(" " + roomDetails.getNumberStudent());
        jLb_FreeSlot.setText(roomDetails.getFreeSlot() + "");
        jLb_ElectricityOld.setText(roomDetails.getElectricityNumberLastSemester() + "");
        jTF_ElectricityNew.setText(" " + roomDetails.getElectricityNumberThisSemester());
        int totalElectricity = roomDetails.getElectricityNumberThisSemester() - roomDetails.getElectricityNumberLastSemester();
        jLb_ElectricityUsed.setText(totalElectricity + "");
        jLb_WaterOld.setText(roomDetails.getWaterNumberLastSemester() + "");
        jTF_WaterNew.setText(" " + roomDetails.getWaterNumberThisSemester());
        int totalWater = roomDetails.getWaterNumberThisSemester() - roomDetails.getWaterNumberLastSemester();
        jLb_WaterUsed.setText(totalWater + "");
    }
    
    private void loadListMember(){
        List<Student> listStudent = new ArrayList();
        listStudent = sm.getStudentsByRoom(roomDetails.getId());        
        rdc.displayListMember(jTable_ListMember, listStudent);
    }

