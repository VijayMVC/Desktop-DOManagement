    private void jPanel_Btn_UpdateMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel_Btn_UpdateMouseClicked
        int electricityNumber = Integer.parseInt(jTF_ElectricityNew.getText().trim());
        if(electricityNumber == roomDetails.getElectricityNumberThisSemester()){
            electricityNumber = 0;            
        }
        
        int waterNumber = Integer.parseInt(jTF_WaterNew.getText().trim());
        if(waterNumber == roomDetails.getWaterNumberThisSemester()){
            waterNumber = 0;
        }
   
        boolean payStatus = false;
        if(jCb_PaymentStatus.getSelectedIndex() == 1){
            payStatus = true;
        }
        int status = jCb_Status.getSelectedIndex();
        int roomId = roomDetails.getId();
        
        boolean isCheck = false;
        isCheck = rm.updateRoomFromModalRoomDetails(electricityNumber, waterNumber, payStatus, status, roomId);
        
        if(isCheck){
            RoomInfo.roomDetails = rm.getRoomById(roomId);
            loadRoomInfo(RoomInfo.roomDetails);
            jLb_Notification.setText("Update room info successfuly !");
        }
    }//GEN-LAST:event_jPanel_Btn_UpdateMouseClicked

